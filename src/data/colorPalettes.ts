// 获取指定品牌的色卡
export function getColorPalette(brand: 'perler' | 'artkal_s' | 'artkal_c'): ColorPalette {
  switch (brand) {
    case 'perler':
      return perlerPalette;
    case 'artkal_s':
      return artkalSPalette;
    case 'artkal_c':
      return artkalCPalette;
    default:
      return perlerPalette;
  }
}

// 获取所有品牌的所有颜色
export function getAllColors(): ColorPalette {
  return {
    ...perlerPalette,
    ...artkalSPalette,
    ...artkalCPalette,
  };
}
